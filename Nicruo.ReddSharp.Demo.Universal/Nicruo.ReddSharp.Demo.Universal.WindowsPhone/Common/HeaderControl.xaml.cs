namespace Nicruo.ReddSharp.Demo.Universal.Common
{
    public sealed partial class HeaderControl : UserControl
    {
        public ICommand Command
        {
            get { return (ICommand)GetValue(CommandProperty); }
            set { SetValue(CommandProperty, value); }
        }
        public static readonly DependencyProperty CommandProperty = DependencyProperty.Register("Command", typeof(ICommand), typeof(HeaderControl), new PropertyMetadata(null));

        public string Title
        {
            get { return (string)GetValue(TitleProperty); }
            set { SetValue(TitleProperty, value); }
        }
        public static readonly DependencyProperty TitleProperty = DependencyProperty.Register("Title", typeof(string), typeof(HeaderControl), new PropertyMetadata(null));

        public HeaderControl()
        {
            this.InitializeComponent();
            root.DataContext = this;
        }
    }
